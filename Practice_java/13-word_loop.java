class Main {
  public static void main(String[] args) {
    System.out.println("loops of java");
     System.out.println("\n this is While loop");
     int num = 1;
    while(num<=5){
      System.out.println(num);
      num++;
    }
     System.out.println("\n this is DO While loop");
     int numb = 1;
     do{
      System.out.println(numb);
      numb++;
    }while(numb<=5);
     System.out.println("\n this is FOR loops of java");

    for(int counter = 10;counter>0;counter--){
      System.out.println(counter);
    }
    System.out.println("\n Now we have left the loop structure");
  }
}
